"""For Loop in Python"""

# Iterating a list:
list=[["Puja","Engineer"],["Dora","Doctor"],["Diya","Biology Master"]]
for items in list:
    print(items)

# Iterating the keys in dictionary:
dict1={
    "Name":"Puja",
    "Address":"Bardhaman",
    "Job":"Engineer"
}

for items in dict1:
    print(items)